def run_tests():
    # Список тестов в формате (исходная строка, ожидаемый результат)
    tests = [
        ("Это тестовая строка с знаками препинания: здесь есть, запятая и точка. Также есть тире-тире!",
         "Это тестовая строка с знаками препинания здесь есть запятая и точка Также есть тире тире"),
        ("Привет, мир!", "Привет мир"),
        ("Я люблю программирование.", "Я люблю программирование"),
        ("Здесь нет знаков препинания", "Здесь нет знаков препинания"),
        ("Строка   с  множественными   пробелами", "Строка с множественными пробелами")  # Добавлен тест с множественными пробелами
    ]

    # Проходим по каждому тесту
    for data, expected in tests:
        # Получаем результат от функции strip_punctuation_ru
        result = strip_punctuation_ru(data)
        # Сравниваем полученный результат с ожидаемым результатом
        if result != expected:
            # Если результат не совпадает с ожидаемым, выводим сообщение о том, что тест не пройден, и выходим из функции
            print(f"Test failed for data: '{data}'. Expected: '{expected}', got: '{result}'")
            print("NO")
            return

    # Если все тесты пройдены успешно, выводим "YES"
    print("YES")

# Запускаем тесты
run_tests()
